
/** API Response Code */
export const enum Code {
    /** Success */
    SUCCESS = 0,
    /** Unknown Error */
    UNKNOWN_ERROR = 1000,
    /** Invalid Parameter */
    INVALID_PARAMETER = 1001,
    /** Not Found */
    NOT_FOUND = 1002,
    /** Already Exists */
    ALREADY_EXISTS = 1003,
    /** Unauthorized */
    UNAUTHORIZED = 1004,
    /** Forbidden */
    FORBIDDEN = 1005,
    /** Internal Server Error */
    INTERNAL_SERVER_ERROR = 1006,
    /** Service Unavailable */
    SERVICE_UNAVAILABLE = 1007,
    /** Timeout */
    TIMEOUT = 1008,
    /** Too Many Requests */
    TOO_MANY_REQUESTS = 1009,
    /** Not Implemented */
    NOT_IMPLEMENTED = 1010,
    
}
